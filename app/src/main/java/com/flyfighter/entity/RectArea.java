package com.flyfighter.entity;


public class RectArea {
    public short x;
    public short y;
    public short w;
    public short h;

    public RectArea() {
        this.x = 0;
        this.y = 0;
        this.w = 0;
        this.h = 0;
    }

    public RectArea(short x, short y, short w, short h) {
        this.x = x;
        this.y = y;
        this.w = w;
        this.h = h;
    }
}
