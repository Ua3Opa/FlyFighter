package com.flyfighter.entity;


public class RectArea {
    public int x;
    public int y;
    public int w;
    public int h;

    public RectArea() {
        this.x = 0;
        this.y = 0;
        this.w = 0;
        this.h = 0;
    }

    public RectArea(int x, int y, int w, int h) {
        this.x = x;
        this.y = y;
        this.w = w;
        this.h = h;
    }
}
